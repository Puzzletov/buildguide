function normalizeTool(tool: Tool): Tool {
  return {
    ...tool,
    icon: ICON_OVERRIDES[tool.id] ?? tool.icon,
    name: repairText(tool.name),
    tagline: repairText(tool.tagline),
    desc: repairText(tool.desc),
    details: {
      Cost: repairText(tool.details.Cost),
      Speed: repairText(tool.details.Speed),
      "Skill needed": repairText(tool.details["Skill needed"]),
      "Best for": repairText(tool.details["Best for"]),
    },
    verdict: {
      type: tool.verdict.type,
      text: repairText(tool.verdict.text),
    },
    badgeLabels: tool.badgeLabels.map(repairText),
    steps: tool.steps.map((step) => ({
      title: repairText(step.title),
      body: repairText(step.body),
    })),
    nextSteps: tool.nextSteps.map(repairText),
  };
}

